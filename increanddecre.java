public class increanddecre {
    public static void main(String[] args) {
        int i = 67;
        System.out.println(i++);
        // pahle hamne i ko use kiya then increment kiya
        System.out.println(i);
        // i ki incremented value ko ab print karvaya hamne
        System.out.println(++i);
        // isme hamne kya kiya pahle hamne jo bhi value thi usko increment kiya then use
        // kiya
        System.out.println(i);
        // isme hamne final value to print karva diya

    }

}
